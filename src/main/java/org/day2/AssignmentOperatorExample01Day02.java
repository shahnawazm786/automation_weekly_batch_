package org.day2;

public class AssignmentOperatorExample01Day02 {
    public static void main(String[] args) {
        // Assignment Operator (=)
        int x=20;
        System.out.println("Value of x is ->"+x);
        // +=
        x+=5; // x=x+5 -> x=20+5 -> x=25
        System.out.println("Value of x is ->"+x);
    }
}
