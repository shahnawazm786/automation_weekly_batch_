package org.day4;

public class LoopControlExample1 {
    public static void main(String[] args) {
        // while loop
        int x=1; // intialize
        while(x<=97){
            System.out.println(x+ "\tGood morning");
            x++; // increment or decrement
        }
        int y=100;
        while(y>=1){
            System.out.println(y+ "\ttimes");
            y++;
        }

    }
}
